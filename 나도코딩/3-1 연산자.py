# 연산자
# 덧셈+ 뺄셈- 곱셈* 나눗셈/ 제곱** 나머지% 몫// 
print(1+1)
print(3-2)
print(5*2)
print(6/3)
print(2**3)
print(5%3)
print(10//3)

# 비교연산
# 크다> 크거나같다>= 작다< 작거나같다<= 같다== 다르다!=,not
print(10 > 3)
print(4 >= 7)
print(10 < 3)
print(4 <= 7)
print(3 == 3)
print(3 != 4)
print(not(3==4))

# 그리고and,& 또는or,|
print((3 > 0) and (3 < 5))
print((3 > 0) & (3 < 5))
print(5 > 4 > 7) # 그리고는 연결해서 표현 가능
print((3 > 0) or (3 > 5))
print((3 > 0) | (3 > 5))